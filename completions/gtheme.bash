_gtheme() {
    local i cur prev opts cmds
    COMPREPLY=()
    cur="${COMP_WORDS[COMP_CWORD]}"
    prev="${COMP_WORDS[COMP_CWORD-1]}"
    cmd=""
    opts=""

    for i in ${COMP_WORDS[@]}
    do
        case "${i}" in
            "$1")
                cmd="gtheme"
                ;;
            add)
                cmd+="__add"
                ;;
            apply)
                cmd+="__apply"
                ;;
            desktop)
                cmd+="__desktop"
                ;;
            disable)
                cmd+="__disable"
                ;;
            enable)
                cmd+="__enable"
                ;;
            extra)
                cmd+="__extra"
                ;;
            fav)
                cmd+="__fav"
                ;;
            help)
                cmd+="__help"
                ;;
            install)
                cmd+="__install"
                ;;
            invert)
                cmd+="__invert"
                ;;
            list)
                cmd+="__list"
                ;;
            pattern)
                cmd+="__pattern"
                ;;
            remove)
                cmd+="__remove"
                ;;
            theme)
                cmd+="__theme"
                ;;
            toggle)
                cmd+="__toggle"
                ;;
            *)
                ;;
        esac
    done

    case "${cmd}" in
        gtheme)
            opts="-h -V -v --help --version --verbose theme desktop pattern extra fav help"
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 1 ]] ; then
                COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
                return 0
            fi
            case "${prev}" in
                *)
                    COMPREPLY=()
                    ;;
            esac
            COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
            return 0
            ;;
        gtheme__desktop)
            opts="-h -v --help --verbose list install help"
